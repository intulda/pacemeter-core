    private boolean shouldSuppressKnownSourceStaleOtherTargetTrackedTargetSplit(
            DotTickRaw dot,
            boolean acceptedBySource,
            List<TrackedDotState> trackedDots
    ) {
        KnownSourceTrackedTargetSplitFeatures features =
                analyzeKnownSourceTrackedTargetSplit(dot, acceptedBySource, trackedDots);
        if (!features.eligible()) {
            return false;
        }
        if (features.sourceTrackedDots().isEmpty()) {
            return false;
        }
        if (features.foreignSourceCount() == 0L) {
            return false;
        }
        SourceDotEvidence actionEvidence = features.actionEvidence();
        SourceDotEvidence statusEvidence = features.statusEvidence();
        if (actionEvidence == null || statusEvidence == null) {
            return false;
        }
        Instant cutoff = dot.ts().minusMillis(KNOWN_SOURCE_STALE_OTHER_TARGET_WINDOW_MS);
        if (actionEvidence.appliedAt().isAfter(cutoff) || statusEvidence.appliedAt().isAfter(cutoff)) {
            return false;
        }
        if (actionEvidence.targetId() == dot.targetId() || statusEvidence.targetId() == dot.targetId()) {
            return false;
        }
        return actionEvidence.targetId() == statusEvidence.targetId();
    }

